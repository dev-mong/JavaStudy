package ifEx;

public class IfEx5 {

	public static void main(String[] args) {

		int n = 4;

		if (n == 1) {
			System.out.println("현재 인원은 1명 입니다.");
		} else if (n == 2) {
			System.out.println("현재 인원은 2명 입니다.");
		} else if (n == 3) {
			System.out.println("현재 인원은 3명 입니다.");
		} else if (n > 3) {
			System.out.println("현재 많은 사람들이 있습니다.");
		}

	}

}
